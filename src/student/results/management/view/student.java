    private void checkRole() {
        switch(role) {
            case "Manager":
                btnDepartment.setEnabled(false);
                btnUser.setEnabled(false);
                break;
            case "Lecturer":
                btnDepartment.setEnabled(false);
                btnUser.setEnabled(false);
                btnBatch.setEnabled(false);
                btnModule.setEnabled(false);
                break;
        }
    }
    
    private void fillTable() throws SQLException{
        StudentController s = new StudentController();
        tblStudent.setModel(DbUtils.resultSetToTableModel(s.getAllData()));  
    }
    
    private void fillBatch() throws SQLException {
        Batch b = new Batch();
        ResultSet rs = b.selectAll();
        while(rs.next())
        {
            cbBatch.addItem(rs.getString(1) + " - " + rs.getString(3));
        }
    }
    
    private void fillDepartment() throws SQLException {
        Department d = new Department();
        ResultSet rs = d.selectAll();
        while(rs.next())
        {
            cbDepartment.addItem(rs.getString(1) + " - " + rs.getString(2));
        }
    }

